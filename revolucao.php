      <p>
        A morte dos jovens foi o estopim que no dia 9 de julho de 1932 deu início à Revolução. Com o apoio dos meios de comunicação, o estado de São Paulo ganha força e consegue mobilizar 35 mil homens, contra 100 mil soldados do governo Vargas.
      </p>

      <p>
        Com todo esse contexto histórico emaranhado em nosso pensamento chega o momento de entrar no Túnel da Mantiqueira, dado esse ponto seguimos o percurso com os pelos dos braços arrepiados e com uma sensação estranha de medo e curiosidade, existem muitas lendas locais sobre o que jaz em tamanha escuridão, sob os trilhos caminhamos em uma linha imperceptível que liga o presente e o passado.
      </p>

      <p>
        Com a luz de uma lanterna é possível observar as paredes do Túnel, água corrente passa por debaixo dos trilhos, gotas caem a todo o momento do teto, a umidade fez com que se formassem desenhos nas paredes. Para uns aquelas marcas são "espíritos", fantasmas das pessoas que alí morreram, conhecido pelos moradores locais como túnel da morte, mas, talvez seja apenas algo que se materializou em forma de gravura, marcas causadas pela infiltração. "Muitas coisas aconteceram aqui teve muito sofrimento, homens morreram, e para construir este túnel, muitas vidas foram perdidas", conta o senhor Sebastião se referindo aos construtores do Túnel da Mantiqueira.
      </p>

      <p>
        Em muitos momentos, quando todos calados, o único som que escutamos foram de nossos passos se chocando contra os trilhos e com a água, um pingo caía aqui o outro acolá, o dia frio de um fim de inverno, mas que contrariava a sensação térmica do exterior do túnel, coisas da inconstância das condições climáticas da região da mantiqueira,  podia ser comparada com o primeira geada que, em forma de uma brisa suave, mas gelada, estremecia nosso corpo aquecido pela caminhada até o decorrer do túnel.  O Tião do Picolé, como é conhecido pelas redondezas, sempre caminha a frente usando um galho grande e grosso de árvore, que achou no chão, pelo meio do caminho, como apoio para ajudar a caminhar. Parece conhecer o caminho como a “palma de suas mãos”, que mesmo trêmulas, castigadas pelo parkinson, o auxilia com seu “cajado”.
      </p>

      <p>
        Um pé na frente do outro com muito cuidado, passo a passo já era possível começar a ver a luz do dia, o sol brilhava lá fora, no início, a escuridão ainda era grande a luz mais se parecia com um grão de areia que crescia a cada instante, aos poucos foi tomando forma e conseguimos enxergar a saída. De repente tudo ficou claro. A escuridão passou e nos demos conta que fizemos mais que uma viagem no passado, mas uma viagem entre mundos e visões diferentes, relembrando um tempo em que pessoas lutaram por um ideal. Ao entrar no Túnel estávamos no estado de São Paulo, agora como em um piscar de olhos chegamos a Minas Gerais. A sensação é que estivemos em dois lugares ao mesmo tempo, presos por uma única linha que os unia o marco na história.
      </p>

      <p>
