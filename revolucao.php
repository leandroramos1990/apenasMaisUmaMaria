      <p>
        "Muitos soldados morreram aqui dentro, era um lugar de esconderijo. No escuro, ficavam agachados nas lacunas, esperando o inimigo passar, não é atoa que aqui tem uma sensação diferente". Conta o senhor Sebastião de Oliveira Freitas de 79 anos, irmão da Dona Maria Castilho, que não pode estar presente na caminhada pelo Túnel da Mantiqueira, por conta da sua idade, saúde e das condições do lugar. Senhor Sebastião foi o guia desse mergulho na história da Revolução de 32, com um olhar atento e de certo modo longe adentrou no túnel úmido e frio.
      </p>
      <p>
        O Túnel da Mantiqueira é um túnel ferroviário com 1 km de extensão, fica localizado na fronteira entre os estados de São Paulo e Minas Gerais de um lado a cidade de Cruzeiro-SP e do outro Passa Quatro-MG.
        O célebre Túnel da Mantiqueira foi palco de umas das maiores batalhas da Revolução de 32 e ao percorrer este trecho percebemos que muitos vestígios daqueles dias longínquos ainda se preservam ali.
      </p>

      <p>
        É de grande relevância voltar um pouco mais no passado para contar essa história, os motivos que iniciaram o movimento datam de dois anos antes como conta Claudio Fernandes da Silva curador do museu da Revolução de 32 da cidade de Cachoeira Paulista que nos ajuda a entender os motivos que levaram a revolução, “ no ano de 1930 o presidente Washington Luís (1926-1930), que era representante dos paulistas, rompeu a aliança com os mineiros que foi realizada no ano de 1889 a chamada "política do café com leite", que era a união entre os estados mais influentes do Brasil na época”. O curador explica que “São Paulo era o maior produtor de Café, conhecido como ouro negro e exportava o produto para o mundo inteiro, São Paulo era a grande locomotiva que movia esse país e minas o maior produtor de leite. São Paulo e Minas Gerais colocavam seus representantes e os mesmos  se alternavam no posto da presidência da república”. Com o rompimento da aliança o estado de São Paulo indica Júlio Prestes como sucessor que venceu as eleições presidenciais, porém as oligarquias mineiras não aceitaram o resultado da votação e, por meio de um golpe de estado articulado com os estados do Rio grande do Sul e da Paraíba, colocam Getúlio Vargas no poder.
      </p>

      <p>
        O curador Cláudio relata que “ Getúlio vargas aproveitou que Washington Luís estava saindo e entrou no lugar dele, Washington Luís podia ter impedido mas ele não o fez, não deixou que Júlio Prestes assumisse o poder, quando vargas entrou eles falaram que ele ia ser um presidente provisório até as novas eleições porém  ele cortou isso tudo, não teve nada do que foi dito”.  O  governo federal ao assumir teve como uma de suas primeiras medidas dissolver o congresso e os poderes estaduais e enquanto isso os outros estados brasileiros ganharam interventores.  O curador Cláudio explica que “eram fortes as pressões sobre o governo Vargas. As frentes econômicas e políticas do estado de São Paulo se uniram e exigiam uma nova assembleia constituinte, novas eleições presidenciais e o fim do governo provisório”. No período de dois anos passaram pelo governo do estado de São Paulo quatro interventores, mas nenhum deles conseguiu manter o controle. As intervenções que estavam ocorrendo por meio da ditadura de Vargas eram cada vez mais constantes e desagradaram cada vez mais à oposição em São Paulo.
      </p>

      <p>
        Claúdio nos conta que no ano de 1931, se junta à elite do estado de São Paulo um grupo nomeado de "grupo mais moderno", que exige que o governo crie uma carta magna que regesse a legislação do Brasil além de eleições para a presidência do país. “Nesse mesmo momento da história os opositores chamados de tenentistas se fortalecem no estado de São Paulo, constituído não somente de militares, mas, também de civis que agiam sob sua liderança” relata o curador. Na data de 23 de maio de 1932, essas duas forças se encontraram e houve um confronto nas ruas da cidade de São Paulo, que teve como resultado a morte de quatro estudantes em praça pública que ficaram conhecidos como MMDC (essa sigla corresponde às iniciais dos nomes dos jovens mortos no confronto: Martins, Miragaia, Dráusio e Camargo). Ao passar do tempo também se adicionou ao final da sigla a Letra A, de Alvarenga, que foi outro jovem que perdeu a sua vida por causa do conflito. Segundo o curador Cláudio “morreram vários jovens, mas pra fazer uma propaganda legal que a propaganda é a alma do negócio, a mídia colocou as iniciais MMDC, mas morreram mais que isso foram mais de 11 jovens mortos naquele dia, colocaram isso para chamar o povo para a revolução”.
      </p>
