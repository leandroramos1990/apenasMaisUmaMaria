          <h3 class="sub-title-aside second">2º dia: Visita ao túnel da revolução</h3>

          <p>
            É feriado de 7 de setembro, está um dia quente de final de inverno. Vamos para a caminhada no Túnel da Mantiqueira, meu avô e minha amiga Isabelle estão comigo. Meu avô diz conhecer bem o caminho. Chegamos à Santa que fica no estado de São Paulo próximo a divisa com o estado de Minas Gerais, “a Santa” é uma imagem grande de Nossa Senhora Aparecida que fica em um mirante próximo ao marco da revolução de 1932. Como é bonito olhar daqui de cima e ver esse vale, a serra da Mantiqueira é um lugar deslumbrante.
          </p>

          <p>
            Do lado do mirante da Santa tem uma estradinha bem estreita de chão que desce para a entrada do Túnel da Mantiqueira que fica localizada no estado de São Paulo. Meu avô Sebastião diz conhecer bem o caminho, conta que o fez várias vezes em sua mocidade.
          </p>

          <p>
            Estranhamos pois já estamos caminhando por 1 hora e ainda não localizamos a entrada do túnel, o caminho é muito bonito, pássaros cantam e a natureza é abundante, muitas árvores a paisagem é de tirar o fôlego. Paramos para beber água em uma nascente cristalina.
          </p>

          <p>
            Caminhamos mais uns 10 minutos e avistamos um homem de meia idade cuidando de sua plantação, meu avô pergunta onde estamos e ele diz que já passamos a um bom tempo da entrada do túnel. O mesmo logo se prontificou a nos levar por dentro de sua propriedade até a entrada do túnel da Mantiqueira. A linha de ferro corta por dentro de suas terras e caminhamos por entre os trilhos até o túnel, o senhor que nos ajuda cortar um  cacho de banana madura e vamos comendo pelo caminho.
          </p>

          <p>
            Ao passar pelo que de início me parece um punhado de entulho, tijolos antigos montados ele me explica que na verdade é  um cemitério abandonado de pessoas que morreram na revolução, mais a frente nos deparamos com as ruínas do era a estão antes do túnel na cidade de Cruzeiro no estado de São Paulo. Ao chegar na entrada do túnel entendemos o porque passamos por ela sem ver, o mato toma conta de tudo é um completo abandono e descaso. Agradecemos ao Senhor que nos ajudou e entramos no túnel.
          </p>

          <p>
            Meu avô vai na frente com um galho que achou no chão que agora o ajuda a caminhar. Não consigo parar de pensar nas histórias que já ouvi sobre esse lugar são muitas as lendas contadas.
          </p>

          <p>
            O interior do túnel é húmido e frio, eu fico arrepiada a todo momento é como se um vento passasse por mim  a todo instante, gotas do teto caem em meu rosto e no meu braço, já não enxergo mais a luz do dia caminho com ajuda de uma lanterna. A única coisa que eu gostaria de ver naquele momento era a luz no fim do túnel, o pavor por vezes toma conta de meu pensamento é um longo percurso sem ver a luz do dia.
          </p>

          <p>
            O silêncio toma conta da nossa caminhada, mas é quebrado várias vezes pelos sons de pingos e de nossos passos. Meu avô caminha sempre a frente com passos rápidos e firmes, aos poucos começo a perceber que lá no fundo tem uma luz que vai crescendo a cada passo que dou.
          </p>

          <p>
            Chegamos no final do túnel e o calor  do sol já toma conta do meu corpo, como é bom ver a luz do dia novamente. Entramos no túnel em Cruzeiro no interior de São Paulo, agora estamos em Passa Quatro no estado de Minas Gerais. O estado de conservação do túnel da Mantiqueira em Minas Gerais é sem dúvidas muito melhor que o de São Paulo, o mato ao redor está cortado, tudo bem limpo, a estação está restaurada e conservada, é um outro cenário bem diferente do que se encontra no outro lado da divisa.
          </p>

          <p>
            Ao fazermos o trajeto para voltar para a Santa rabisco em um caderno: Brava gente brasileira, heróis da Mantiqueira, homens que lutaram uns até a morte, outros que contaram com a sorte, e dias nas montanhas guerrilharam, por três meses lá ficaram, as terras de lá deixaram, hoje são lendas, mitos e história e sempre se farão presentes em minha memória.
          </p>

          <p>
            Ao passar em frente ao memorial da revolução de 1932 no estado de Minas Gerais li a seguinte frase que fez todo o sentido em meio as minhas sensações e pensamentos: “Tu que passas, descobre-te: aqui dorme o forte que morreu”.
          </p>
